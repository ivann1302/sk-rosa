const groupBySlug = Object.fromEntries(
  Object.entries(cityGroups).flatMap(([group, slugs]) => slugs.map(slug => [slug, group])),
);

export function getLocalCityProfile(city) {
  const group = groupBySlug[city.slug] ?? "mixedTown";

  return {
    ...defaultProfile,
    ...groupProfiles[group],
    ...(cityOverrides[city.slug] ?? {}),
    group,
  };
}
