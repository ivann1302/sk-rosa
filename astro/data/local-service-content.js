function hash(value) {
  return [...value].reduce((acc, char) => (acc * 31 + char.charCodeAt(0)) % 9973, 7);
}

function pickOne(items, seed) {
  return pick(items, seed, 1)[0];
}

function pick(items, seed, count) {
  if (items.length <= count) {
    return items;
  }

  const offset = hash(seed) % items.length;
  return Array.from({ length: count }, (_, index) => items[(offset + index) % items.length]);
}

function sentenceList(items) {
  if (items.length <= 1) {
    return items[0] ?? "";
  }

  const joiner = items.some(item => item.includes(" и ")) ? ", а также " : " и ";

  return `${items.slice(0, -1).join(", ")}${joiner}${items.at(-1)}`;
}

function complexText(city, complexes, seed) {
  if (complexes.length === 0) {
    return pickOne(
      [
        `Если объект находится в новом доме, заранее смотрим правила разгрузки, режим ремонтных работ и доступ к лифту. Для частных домов в ${city.nameIn} отдельно оцениваем подъезд к участку и место для материалов.`,
        `Когда объект без привязки к жилому комплексу, на замере в ${city.nameIn} отдельно уточняем подъезд, место под материалы и ограничения по шумным работам.`,
        `Для частных домов и отдельных помещений в ${city.nameIn} важно заранее понять, где разгружать материалы, как подать оборудование и какие зоны нужно защитить до начала работ.`,
      ],
      `${seed}:no-complexes`,
    );
  }

  const sample = complexes.slice(0, 4);
  return pickOne(
    [
      `В ${city.nameIn} учитываем специфику жилых комплексов: ${sentenceList(sample)}. На таких объектах заранее проверяем правила разгрузки, лифт, защиту общих зон и ограничения по шумным работам.`,
      `Если объект находится в одном из комплексов: ${sentenceList(sample)}, заранее смотрим регламент дома: время шумных работ, подъём материалов, защиту лифта и места прохода.`,
      `Для объектов в комплексах ${sentenceList(sample)} важны не только сами работы, но и организация: пропуск, разгрузка, складирование материалов и аккуратность в общих зонах.`,
    ],
    `${seed}:complexes`,
  );
}

function buildBlocks({
  city,
  profile,
  service,
  vocabulary,
  complexes,
  objectMix,
  planningFocus,
  priceFactors,
  scenarios,
  nearby,
  seed,
  angle,
  priority,
  risk,
  quality,
  readiness,
}) {
  const blockFactories = {
    objects: () => ({
      title: pickOne(
        [
          `Какие объекты чаще берём в ${city.nameIn}`,
          `Типовые объекты и условия в ${city.nameIn}`,
          `Где чаще нужна услуга в ${city.nameIn}`,
        ],
        `${seed}:objects-title`,
      ),
      paragraphs: [
        `${profile.demand} В ${city.nameIn} типичны ${sentenceList(objectMix)}, поэтому сценарий работ подбираем под конкретный объект, а не по общей таблице.`,
        `${complexText(city, complexes, seed)} В таких условиях отдельно учитываем такой фактор: ${priority}.`,
      ],
    }),
    measure: () => ({
      title: pickOne(
        ["Что проверяем на бесплатном замере", "С чего начинаем осмотр", "Что уточняем до сметы"],
        `${seed}:measure-title`,
      ),
      items: [
        service.measureFocus,
        `учитываем местный контекст: ${angle}`,
        ...planningFocus.map(item => `учитываем ${item}`),
        `проверяем готовность: ${readiness}`,
      ],
      note: `После замера фиксируем объём работ по услуге «${service.workName}» и отдельно отмечаем спорные места, если они есть.`,
    }),
    price: () => ({
      title: pickOne(
        ["Что влияет на смету", "Из чего складывается стоимость", "Какие факторы считаем отдельно"],
        `${seed}:price-title`,
      ),
      items: priceFactors,
      note: `После осмотра в ${city.nameIn} даём смету с понятной разбивкой. Особенно важно: ${risk}.`,
    }),
    scenarios: () => ({
      title: pickOne(
        ["Типовые задачи", "С какими задачами приезжаем", "Какие сценарии работ встречаются"],
        `${seed}:scenarios-title`,
      ),
      items: scenarios,
      note: `На сдаче ориентируемся не только на факт выполнения, но и на результат: ${quality}.`,
    }),
    nearby: () => ({
      title: pickOne(
        ["Выезд и соседние направления", "Как организуем выезд", "Логистика и близкие города"],
        `${seed}:nearby-title`,
      ),
      paragraphs: [
        `${profile.logistics} Бригада приезжает с инструментом и согласованным списком материалов, чтобы не тратить день на организационные вопросы.`,
        `Кроме объектов в ${city.nameIn}, можем планировать близкие направления: ${sentenceList(nearby)}. Это удобно, если объект находится не в самом центре города, а в посёлке, ЖК или частном секторе рядом.`,
      ],
    }),
  };

  const order = pickOne(blockOrderVariants, `${seed}:block-order`);

  return order.map(key => blockFactories[key]());
}

export function buildLocalServiceContent({ city, serviceSlug, complexes = [] }) {
  const profile = getLocalCityProfile(city);
  const service = serviceProfiles[serviceSlug];
  const vocabulary = serviceVocabulary[serviceSlug];

  if (!service || !vocabulary) {
    throw new Error(`Unknown local service profile: ${serviceSlug}`);
  }

  const seed = `${city.slug}:${serviceSlug}`;
  const objectMix = pick(vocabulary.objects ?? profile.objectMix, `${seed}:objects`, 3);
  const planningFocus = pick(profile.planningFocus, `${seed}:planning`, 3);
  const priceFactors = pick(service.priceFactors, `${seed}:price`, 4);
  const scenarios = pick(service.scenarios, `${seed}:scenarios`, 3);
  const nearby = pick(profile.nearby, `${seed}:nearby`, 3);
  const primaryObject = pickOne(objectMix, `${seed}:primary-object`);
  const primaryScenario = pickOne(scenarios, `${seed}:primary-scenario`);
  const angle = pickOne(groupAngles[profile.group] ?? groupAngles.mixedTown, `${seed}:angle`);
  const priority = pickOne(vocabulary.priorities, `${seed}:priority`);
  const risk = pickOne(vocabulary.risks, `${seed}:risk`);
  const quality = pickOne(vocabulary.quality, `${seed}:quality`);
  const readiness = pickOne(vocabulary.readiness, `${seed}:readiness`);
  const lead = pickOne(leadTemplates, `${seed}:lead`)({
    city,
    profile,
    service,
    primaryObject,
    primaryScenario,
    angle,
    priority,
    risk,
    quality,
  });

  return {
    title: `Локальная специфика: ${service.workName} в ${city.nameIn}`,
    lead,
    blocks: buildBlocks({
      city,
      profile,
      service,
      vocabulary,
      complexes,
      objectMix,
      planningFocus,
      priceFactors,
      scenarios,
      nearby,
      seed,
      angle,
      priority,
      risk,
      quality,
      readiness,
    }),
    faq: [
      {
        question: `Вы выезжаете на замер в ${city.nameIn}?`,
        answer: `Да, выезжаем на замер в ${city.nameIn} и рядом расположенные населённые пункты. На месте оцениваем объём работ и доступ к объекту, отдельно смотрим: ${priority}; после этого готовим смету.`,
      },
      {
        question: `Сколько стоит ${service.questionName} в ${city.nameIn}?`,
        answer: `Цена зависит не только от площади. В смете для объекта в ${city.nameIn} отдельно показываем: ${sentenceList(priceFactors.slice(0, 3))}; дополнительно проверяем, что может повлиять на срок и подготовку. Точную стоимость фиксируем после замера.`,
      },
      {
        question: `Можно ли начать работы в ${city.nameIn} без предварительной подготовки?`,
        answer: `Иногда можно, но чаще сначала нужно проверить: ${readiness}. Поэтому перед стартом проводим осмотр, отдельно проговариваем: ${risk}; затем даём понятный список подготовки.`,
      },
    ],
  };
}
