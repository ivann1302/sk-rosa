function hash(value) {
  return [...value].reduce((acc, char) => (acc * 31 + char.charCodeAt(0)) % 9973, 7);
}

function pickOne(items, seed) {
  return pick(items, seed, 1)[0];
}

function pick(items, seed, count) {
  if (items.length <= count) {
    return items;
  }

  const offset = hash(seed) % items.length;
  return Array.from({ length: count }, (_, index) => items[(offset + index) % items.length]);
}

function sentenceList(items) {
  if (items.length <= 1) {
    return items[0] ?? "";
  }

  const joiner = items.some(item => item.includes(" и ")) ? ", а также " : " и ";

  return `${items.slice(0, -1).join(", ")}${joiner}${items.at(-1)}`;
}

